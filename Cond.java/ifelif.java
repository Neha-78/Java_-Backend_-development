public class ifelif {
    public static void main(String[] args) {
         int x =5;
         int y = 7;
         int z = 6;

         if(x>y){
            System.out.println(x);
         }
         else if(y>z){
            System.out.println(y);
         }
         else{
            System.out.println(z);
         }
    }
    
}
